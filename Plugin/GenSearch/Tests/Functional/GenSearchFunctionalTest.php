declare(strict_types=1);

namespace Apisearch\Plugin\GenSearch\Tests\Functional;

use Apisearch\Plugin\GenSearch\GenSearchPluginBundle;
use Apisearch\Server\Tests\Functional\ServiceFunctionalTest;

/**
 * Class GenSearchFunctionalTest.
 */
abstract class GenSearchFunctionalTest extends ServiceFunctionalTest
{
    /**
     * Decorate bundles.
     *
     * @param array $bundles
     *
     * @return array
     */
    protected static function decorateBundles(array $bundles): array
    {
        $bundles[] = GenSearchPluginBundle::class;

        return $bundles;
    }

    /**
     * Decorate configuration.
     *
     * @param array $configuration
     *
     * @return array
     */
    protected static function decorateConfiguration(array $configuration): array
    {
        $configuration = parent::decorateConfiguration($configuration);
        $configuration['imports'][] = ['resource' => '@GenSearchPluginBundle/Resources/test/repository.yml'];

        return $configuration;
    }
}
