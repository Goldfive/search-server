declare(strict_types=1);

namespace Apisearch\Plugin\MetadataFields\DependencyInjection;

use Mmoreram\BaseBundle\DependencyInjection\BaseConfiguration;
use Symfony\Component\Config\Definition\Builder\ArrayNodeDefinition;

/**
 * Class MetadataFieldsPluginConfiguration.
 */
class MetadataFieldsPluginConfiguration extends BaseConfiguration
{
    /**
     * Configure the root node.
     *
     * @param ArrayNodeDefinition $rootNode Root node
     */
    protected function setupTree(ArrayNodeDefinition $rootNode)
    {
        $rootNode
            ->children()
                ->scalarNode('repository_service')
                    ->defaultValue('apisearch_plugin.metadata_fields.redis_repository')
                ->end();
    }
}
